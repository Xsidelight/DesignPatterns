package CommandPattern.Example;

public class GarageDoor {
    public void up() {
        System.out.println("A Garage door is open");
    }

    public void down() {
        System.out.println("A Garage door is closed");
    }

    public void stop() {
        System.out.println("Stopped opening the garage door");
    }

    public void lightOn() {
        System.out.println("Light is on");
    }


    public void lightOff() {
        System.out.println("Light is off");
    }

}
